#!/usr/bin/env python3
"""Print a status table for every known GAMBIT backend interface.
Reads config/gambit_backends.yaml (regenerated by backend_harvester.py) for
the enabled/disabled interface status, and the data file written by
CMakeLists.txt at configure time (TARGETS=<cmake-list>) for the set of
active backend make targets. Walks every Bit's include/src/examples tree
to find which Bits reference each backend. Prints one row per canonical
BACKENDNAME with its status, a "used by:" list of Bits, and the
backend's user-invokable make targets.
"""
from __future__ import print_function

import io
import os
import sys

import yaml

_HERE = os.path.dirname(os.path.abspath(__file__))
sys.path.insert(0, os.path.normpath(os.path.join(_HERE, "..", "..", "Utils", "scripts")))

from harvesting_tools import (  # noqa: E402
    compute_bits_per_backend,
    get_all_backendnames,
)


def main():
    if len(sys.argv) != 2:
        sys.stderr.write("Usage: list_backends.py <data_file>\n")
        sys.exit(2)

    active_targets = []
    build_dir = ""
    with io.open(sys.argv[1], "r") as f:
        for line in f:
            if line.startswith("TARGETS="):
                active_targets = [s for s in line[len("TARGETS="):].rstrip("\n").split(";") if s]
            elif line.startswith("BUILD_DIR="):
                build_dir = line[len("BUILD_DIR="):].rstrip("\n").strip()

    def is_target_installed(target):
        """ExternalProject_Add writes a <target>-done stamp into its stamp dir
        once configure/build/install all succeed; gambit's clean/nuke targets
        remove it, so the stamp tracks reality."""
        if not build_dir or not target:
            return False
        return os.path.exists(os.path.join(
            build_dir, target + "-prefix", "src",
            target + "-stamp", target + "-done"))

    with io.open("./config/gambit_backends.yaml", "r") as f:
        bdata = yaml.safe_load(f) or {}
    enabled_set = set((bdata.get("enabled") or {}).keys())

    frontend_dir = "./Backends/include/gambit/Backends/frontends"
    all_backends = get_all_backendnames(frontend_dir)
    bits_per = compute_bits_per_backend(".", frontend_dir)

    # Group each active target under its longest-matching canonical (so e.g.
    # darksusy_MSSM_6.4.0 lands under DarkSUSY_MSSM, not under DarkSUSY).
    canonicals_by_len = sorted(all_backends, key=len, reverse=True)
    targets_by_canonical = {b: [] for b in all_backends}
    for t in active_targets:
        tl = t.lower()
        matched = False
        for canonical in canonicals_by_len:
            if tl.startswith(canonical.lower() + "_"):
                targets_by_canonical[canonical].append(t)
                matched = True
                break
        # Fallback: try the collapsed-underscore form, for canonicals where
        # the make target drops internal underscores (e.g. SUSY_HIT
        # -> susyhit_1.5).
        if not matched:
            for canonical in canonicals_by_len:
                cl = canonical.replace("_", "").lower()
                if cl != canonical.lower() and tl.startswith(cl + "_"):
                    targets_by_canonical[canonical].append(t)
                    break

    rows = []
    for canonical in sorted(all_backends, key=lambda s: s.lower()):
        targets = sorted(targets_by_canonical[canonical])
        active = canonical in enabled_set
        bits = bits_per.get(canonical, [])
        rows.append((canonical, active, bits, targets))

    name_w = max(len(r[0]) for r in rows)
    name_w = max(name_w, len("Backend"))
    bits_w = max(len(", ".join(r[2]) if r[2] else "none") for r in rows)
    # "used by: " column spans the literal prefix (9 chars) plus the padded
    # bits string. Bump to fit the "Used by Bits" header if narrower.
    used_by_col_w = max(9 + bits_w, len("Used by Bits"))
    bits_w = used_by_col_w - 9
    use_color = sys.stdout.isatty()
    YELLOW = "\033[33m" if use_color else ""
    GREEN  = "\033[32m" if use_color else ""
    CYAN   = "\033[36m" if use_color else ""
    DIM    = "\033[2m"  if use_color else ""
    BOLD   = "\033[1m" if use_color else ""
    RESET  = "\033[0m" if use_color else ""

    # Status column tags: width 15 to fit "[not installed]" (longest visible
    # label). ANSI codes don't add visible width, so explicit space-padding
    # after the colored bracket keeps every row aligned.
    TAG_W = len("[not installed]")
    def make_tag(label, color):
        if not label:
            return " " * TAG_W
        return color + label + RESET + " " * (TAG_W - len(label))

    # Header row + dashed separator.
    print("  {bold}{h1:<{nw}}  {h2:<{tw}}  {h3:<{w3}}  {h4}{reset}".format(
        bold=BOLD, reset=RESET,
        h1="Backend", nw=name_w,
        h2="Status", tw=TAG_W,
        h3="Used by Bits", w3=used_by_col_w,
        h4="Make targets"))
    print("  {0}  {1}  {2}  {3}".format(
        "-" * name_w, "-" * TAG_W, "-" * used_by_col_w, "-" * len("Make targets")))

    for name, active, bits, targets in rows:
        # Bits column: dim "none" when empty. ANSI codes don't take visible
        # width, so do the column-padding manually rather than via {:<{bw}}.
        if bits:
            bits_visible = ", ".join(bits)
            bits_field   = bits_visible + " " * (bits_w - len(bits_visible))
        else:
            bits_field = DIM + "none" + RESET + " " * (bits_w - len("none"))

        # Targets column: dim "none" when no targets; otherwise annotate
        # individually-installed targets inline.
        if not targets:
            targets_field = DIM + "none" + RESET
        else:
            ann = []
            for t in targets:
                if is_target_installed(t):
                    ann.append("{} [installed]".format(t))
                else:
                    ann.append(t)
            targets_field = ", ".join(ann)

        if not active:
            kind = "disabled"
        elif targets and any(is_target_installed(t) for t in targets):
            kind = "installed"
        elif targets:
            kind = "not_installed"
        else:
            kind = ""
        tag_color = {"disabled": YELLOW, "installed": GREEN,
                     "not_installed": CYAN, "": ""}[kind]
        tag_label = {"disabled": "[disabled]", "installed": "[installed]",
                     "not_installed": "[not installed]", "": ""}[kind]
        tag = make_tag(tag_label, tag_color)
        print("  {name:<{nw}}  {tag}  used by: {bits}  targets: {tgts}".format(
            name=name, nw=name_w, tag=tag, bits=bits_field, tgts=targets_field))


if __name__ == "__main__":
    main()